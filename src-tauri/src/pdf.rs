// ── Filter ────────────────────────────────────────────────────────────────────

/// True for paths matching `.../active/reports/*.md`.
pub(crate) fn is_active_report(path: &str) -> bool {
    let p = Path::new(path);
    if p.extension().and_then(|e| e.to_str()) != Some("md") {
        return false;
    }
    let Some(parent) = p.parent() else {
        return false;
    };
    let Some(grandparent) = parent.parent() else {
        return false;
    };
    parent.file_name().and_then(|n| n.to_str()) == Some("reports")
        && grandparent.file_name().and_then(|n| n.to_str()) == Some("active")
}

// ── Core render ───────────────────────────────────────────────────────────────

pub(crate) fn render_pdf_from_md(md_path: &str) -> Result<String, String> {
    // Guard: reject traversal and wrong extension.
    if md_path.contains("..") {
        return Err("path traversal not allowed".into());
    }
    let path = Path::new(md_path);
    if !path.exists() {
        return Err(format!("file not found: {md_path}"));
    }
    if path.extension().and_then(|e| e.to_str()) != Some("md") {
        return Err("path must end in .md".into());
    }

    // Read markdown; extract title.
    let content = fs::read_to_string(path).map_err(|e| e.to_string())?;
    let title = extract_title_from_md(&content).unwrap_or_else(|| {
        path.file_stem()
            .and_then(|s| s.to_str())
            .unwrap_or("Report")
            .to_string()
    });

    // Render GFM → HTML.
    let mut opts = Options::default();
    opts.extension.table = true;
    opts.extension.strikethrough = true;
    opts.extension.autolink = true;
    opts.extension.tasklist = true;
    let body_html = markdown_to_html(&content, &opts);

    let date = Local::now().format("%B %-d, %Y").to_string();
    let html = build_html(&title, &body_html, &date);

    // Write temp HTML next to the .md file.
    let html_path = path.with_extension("tmp.html");
    fs::write(&html_path, &html).map_err(|e| e.to_string())?;

    // Find a headless Chrome/Chromium browser.
    let chrome_candidates = [
        "/Applications/Google Chrome.app/Contents/MacOS/Google Chrome",
        "/Applications/Chromium.app/Contents/MacOS/Chromium",
        "/Applications/Microsoft Edge.app/Contents/MacOS/Microsoft Edge",
        "/Applications/Brave Browser.app/Contents/MacOS/Brave Browser",
    ];
    let chrome = chrome_candidates
        .iter()
        .find(|&&p| Path::new(p).exists())
        .copied()
        .ok_or_else(|| "No Chrome/Chromium found to render PDF".to_string())?;

    let pdf_path = path.with_extension("pdf");
    let html_url = format!("file://{}", html_path.to_string_lossy());

    let result = std::process::Command::new(chrome)
        .args([
            "--headless=new",
            "--disable-gpu",
            "--no-pdf-header-footer",
            "--virtual-time-budget=2000",
            &format!("--print-to-pdf={}", pdf_path.to_string_lossy()),
            &html_url,
        ])
        .output()
        .map_err(|e| e.to_string());

    // Always delete the temp HTML, even on error.
    let _ = fs::remove_file(&html_path);

    let output = result?;
    if !output.status.success() {
        return Err(format!(
            "Chrome exited with {}: {}",
            output.status,
            String::from_utf8_lossy(&output.stderr)
        ));
    }

    pdf_path
        .to_str()
        .map(|s| s.to_string())
        .ok_or_else(|| "invalid PDF path encoding".to_string())
}

// ── Tauri command ─────────────────────────────────────────────────────────────

#[tauri::command]
pub fn render_report_pdf(md_path: String) -> Result<String, String> {
    render_pdf_from_md(&md_path)
}
