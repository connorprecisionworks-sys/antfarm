use chrono::Local;
use comrak::{markdown_to_html, Options};
use std::fs;
use std::path::Path;

// ── Helpers ───────────────────────────────────────────────────────────────────

fn extract_title_from_md(content: &str) -> Option<String> {
    content
        .lines()
        .find_map(|line| line.trim().strip_prefix("# ").map(|s| s.trim().to_string()))
        .filter(|s| !s.is_empty())
}

fn html_escape(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
}

